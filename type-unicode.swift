import Cocoa

guard CommandLine.arguments.count == 3,
      let inputCP = UInt32(CommandLine.arguments[2]) else { exit(1) }

let mode = CommandLine.arguments[1]  // "sup", "sub", or "direct"

let superMap: [UInt32: UInt32] = [
    // Digits
    0x30: 0x2070, 0x31: 0x00B9, 0x32: 0x00B2, 0x33: 0x00B3,
    0x34: 0x2074, 0x35: 0x2075, 0x36: 0x2076, 0x37: 0x2077,
    0x38: 0x2078, 0x39: 0x2079,
    // Operators
    0x2B: 0x207A, 0x2D: 0x207B, 0x3D: 0x207C, 0x28: 0x207D, 0x29: 0x207E,
    // Lowercase
    0x61: 0x1D43, 0x62: 0x1D47, 0x63: 0x1D9C, 0x64: 0x1D48, 0x65: 0x1D49,
    0x66: 0x1DA0, 0x67: 0x1D4D, 0x68: 0x02B0, 0x69: 0x2071, 0x6A: 0x02B2,
    0x6B: 0x1D4F, 0x6C: 0x02E1, 0x6D: 0x1D50, 0x6E: 0x207F, 0x6F: 0x1D52,
    0x70: 0x1D56, 0x72: 0x02B3, 0x73: 0x02E2, 0x74: 0x1D57, 0x75: 0x1D58,
    0x76: 0x1D5B, 0x77: 0x02B7, 0x78: 0x02E3, 0x79: 0x02B8, 0x7A: 0x1DBB,
    // Uppercase (partial)
    0x41: 0x1D2C, 0x42: 0x1D2E, 0x44: 0x1D30, 0x45: 0x1D31, 0x47: 0x1D33,
    0x48: 0x1D34, 0x49: 0x1D35, 0x4A: 0x1D36, 0x4B: 0x1D37, 0x4C: 0x1D38,
    0x4D: 0x1D39, 0x4E: 0x1D3A, 0x4F: 0x1D3C, 0x50: 0x1D3E, 0x52: 0x1D3F,
    0x54: 0x1D40, 0x55: 0x1D41, 0x56: 0x2C7D, 0x57: 0x1D42,
    // Added: C F Q V (Latin Extended-D / Extended-C)
    0x43: 0xA7F2, 0x46: 0xA7F3, 0x51: 0xA7F4,
]

let subMap: [UInt32: UInt32] = [
    // Digits
    0x30: 0x2080, 0x31: 0x2081, 0x32: 0x2082, 0x33: 0x2083,
    0x34: 0x2084, 0x35: 0x2085, 0x36: 0x2086, 0x37: 0x2087,
    0x38: 0x2088, 0x39: 0x2089,
    // Operators
    0x2B: 0x208A, 0x2D: 0x208B, 0x3D: 0x208C, 0x28: 0x208D, 0x29: 0x208E,
    // Lowercase (subset with Unicode coverage)
    0x61: 0x2090, 0x65: 0x2091, 0x68: 0x2095, 0x69: 0x1D62, 0x6A: 0x2C7C,
    0x6B: 0x2096, 0x6C: 0x2097, 0x6D: 0x2098, 0x6E: 0x2099, 0x6F: 0x2092,
    0x70: 0x209A, 0x72: 0x1D63, 0x73: 0x209B, 0x74: 0x209C, 0x75: 0x1D64,
    0x76: 0x1D65, 0x78: 0x2093,
    // Greek subscripts (input = Greek letter codepoint)
    0x03B2: 0x1D66, 0x03B3: 0x1D67, 0x03C1: 0x1D68, 0x03C6: 0x1D69, 0x03C7: 0x1D6A,
]

let outputCP: UInt32 = mode == "direct" ? inputCP
    : (mode == "sup" ? superMap : subMap)[inputCP] ?? inputCP
guard Unicode.Scalar(outputCP) != nil else { exit(1) }

let appName = NSWorkspace.shared.frontmostApplication?.localizedName ?? ""

if appName == "iTerm2" {
    var err: NSDictionary?
    NSAppleScript(source: "tell application \"iTerm2\" to tell current session of current window to write text (character id \(outputCP)) newline NO")!
        .executeAndReturnError(&err)
} else {
    let pb = NSPasteboard.general
    let saved = pb.string(forType: .string)
    pb.clearContents()
    pb.setString(String(Unicode.Scalar(outputCP)!), forType: .string)
    var err: NSDictionary?
    NSAppleScript(source: "tell application \"System Events\" to keystroke \"v\" using command down")!
        .executeAndReturnError(&err)
    Thread.sleep(forTimeInterval: 0.05)
    pb.clearContents()
    if let s = saved { pb.setString(s, forType: .string) }
}
